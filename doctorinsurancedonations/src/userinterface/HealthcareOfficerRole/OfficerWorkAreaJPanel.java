    public void pplTbl() {
        DefaultTableModel model = (DefaultTableModel) tblWrkReqTbl.getModel();

        model.setRowCount(0);

        for (WorkReq req : hlthCrOfcrJPanel.getWrkQ().getWrkReqs()) {
            Object[] row = new Object[5];
            String status = req.getReqStatus();
            row[0] = ((GovFundReq) req);
            row[1] = req.getSndr().getEmploye().getEmpName();
            if (status.equalsIgnoreCase("Sent to Treasurer") || status.equalsIgnoreCase("Sent to Secretary")) {
                row[2] = null;
            } else {
                row[2] = req.getRcvr() == null ? null : req.getRcvr().getEmploye().getEmpName();
            }
            row[3] = req.getReqStatus();
            row[4] = ((GovFundReq) req).getAmountRequested();

            model.addRow(row);
        }
        TableRowSorter<DefaultTableModel> sorter = new TableRowSorter<>(model);
        tblWrkReqTbl.setRowSorter(sorter);
    }
