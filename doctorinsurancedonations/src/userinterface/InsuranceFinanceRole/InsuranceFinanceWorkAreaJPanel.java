    public void pplTabl() {

        DefaultTableModel model = (DefaultTableModel) tblInsFinance.getModel();
        model.setRowCount(0);

        for (WorkReq workRequest : insrFincOrg.getWrkQ().getWrkReqs()) {
            InsReq insrWkReq = ((InsReq) workRequest);
            Object[] row = new Object[8];
            row[0] = insrWkReq;
            row[1] = insrWkReq.getNameOfPolicy();
            row[2] = insrWkReq.getInsuranceCust().getIns().getCoverage();
            row[3] = insrWkReq.getClmAmt();
            row[4] = insrWkReq.getBillingAmt();
            row[5] = insrWkReq.getReqStatus();
            row[6] = insrWkReq.getInsuranceName();
            row[7] = insrWkReq.getRcvr() == null ? "" : insrWkReq.getRcvr().getEmploye().getEmpName();

            model.addRow(row);
        }
        
        TableRowSorter<DefaultTableModel> sorter = new TableRowSorter<>(model);
        tblInsFinance.setRowSorter(sorter);
    }

