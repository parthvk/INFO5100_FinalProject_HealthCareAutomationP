    private void btnSbmtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSbmtActionPerformed
        String bloodBankMessage = txtMsg.getText().trim();
        if (bloodBankMessage.equals("")) {
            JOptionPane.showMessageDialog(null, "Result is mandatory");
            return;
        } else {
            int dialogResult = JOptionPane.showConfirmDialog(null, "Do you want to proceed?");
            if (dialogResult == JOptionPane.YES_OPTION) {
                ptTrWrReq.setMsgFromBloodBank(bloodBankMessage);
                ptTrWrReq.setReqStatus("Blood Bank Request Completed");
                JOptionPane.showMessageDialog(null, "Result submitted successfully");
                txtMsg.setText("");
                btnSbmt.setEnabled(false);
            }

        }


    }//GEN-LAST:event_btnSbmtActionPerformed

