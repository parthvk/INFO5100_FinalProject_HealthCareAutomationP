    private void btnReqTstActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnReqTstActionPerformed

        String labType = txtLabTyp.getText().trim();
        String message = txtMsg.getText().trim();

        if (labType.equals("")) {
            JOptionPane.showMessageDialog(null, "Lap type is mandatory!");
            return;
        }
        if (message.equals("")) {
            JOptionPane.showMessageDialog(null, "Message is mandatory");
            return;
        }


        pntTrmntWrReq.setMsgFromLabTest(message);
        pntTrmntWrReq.setSndr(usrAcnt);
        pntTrmntWrReq.setReqStatus("SentToLab");
        pntTrmntWrReq.setRcvr(null);

        Organization org = null;
        for (Organization organization : entrpz.getOrgDir().getOrganizations()) {

            if (organization instanceof OrgLab) {
                org = organization;
                break;
            }
        }
        if (org != null) {

            org.getWrkQ().getWrkReqs().add(pntTrmntWrReq);
            usrAcnt.getWorkQ().getWrkReqs().add(pntTrmntWrReq);

            JOptionPane.showMessageDialog(null, "Lab request sent");
            txtMsg.setText("");
            txtLabTyp.setText("");
            btnReqTst.setEnabled(false);
        }

    }//GEN-LAST:event_btnReqTstActionPerformed

