    private void btnSbmtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSbmtActionPerformed
        btnSbmt.setEnabled(true);
        String message = txtMsg.getText();
        
        if (message.equals("")) {
            JOptionPane.showMessageDialog(null, "Message is mandatory!");
            return;
        }
        else
        {
        govtFndRequest.setWrkMsg(message);
         int dialogResult = JOptionPane.showConfirmDialog(null, "Do you want to proceed?");
         if (dialogResult == JOptionPane.YES_OPTION) {
        govtFndRequest.setReqStatus("Accepted");
        JOptionPane.showMessageDialog(null, "Funds Disbursed Successfully!!!");
        btnSbmt.setEnabled(false);
        txtMsg.setText("");
        btnReq.setEnabled(false);
        }
         txtMsg.setText("");
        }

    }//GEN-LAST:event_btnSbmtActionPerformed

