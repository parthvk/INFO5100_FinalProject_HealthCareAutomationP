    private void btnDsbrsAmtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDsbrsAmtActionPerformed
        btnDsbrsAmt.setEnabled(true);
        String message = txtMsg.getText();
        if (message.equals("")) {
            JOptionPane.showMessageDialog(null, "Message is mandatory!");
            return;
        }
        else
        {
        ngoFndReq.setWrkMsg(message);
         int dialogResult = JOptionPane.showConfirmDialog(null, "Do you want to proceed?");
         if (dialogResult == JOptionPane.YES_OPTION) {
        ngoFndReq.setReqStatus("Accepted");
        JOptionPane.showMessageDialog(null, "Funds Disbursed Successfully!!!");
        btnDsbrsAmt.setEnabled(false);
        txtMsg.setText("");
        btnRejReq.setEnabled(false);
        }
         txtMsg.setText("");
        }

    }//GEN-LAST:event_btnDsbrsAmtActionPerformed

    private void btnBackActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBackActionPerformed

        jPnl.remove(this);
        Component[] componentArray = jPnl.getComponents();
        Component component = componentArray[componentArray.length - 1];
        DirectorWorkAreaJPanel dwjp = (DirectorWorkAreaJPanel) component;
        dwjp.populateTable();
        CardLayout layout = (CardLayout) jPnl.getLayout();
        layout.previous(jPnl);
    }//GEN-LAST:event_btnBackActionPerformed

    private void btnRejReqActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRejReqActionPerformed
        String message = txtMsg.getText();
        if (message.equals("")) {
            JOptionPane.showMessageDialog(null, "Message is mandatory!");
            return;
        } else {
        ngoFndReq.setWrkMsg(message);
        int dialogResult = JOptionPane.showConfirmDialog(null, "Do you want to proceed?");
            
            
            if (dialogResult == JOptionPane.YES_OPTION) {
        ngoFndReq.setReqStatus("Rejected");
         txtMsg.setText("");
            btnRejReq.setEnabled(false);
            btnDsbrsAmt.setEnabled(false);
        }
         txtMsg.setText("");   
        }
    }//GEN-LAST:event_btnRejReqActionPerformed


