    public void pplTbl() {
        DefaultTableModel model = (DefaultTableModel) tblWrkReq.getModel();
        double totalFunds = 0;
        model.setRowCount(0);

        for (WorkReq request : usrAc.getWorkQ().getWrkReqs()) {

            if(request instanceof GovFundReq){
            Object[] row = new Object[4];
            row[0] = String.valueOf(((GovFundReq) request).getLoc());
            row[1] = request.getRcvr();
            row[2] = request.getReqStatus();
            row[3] = String.valueOf(((GovFundReq) request).getAmountRequested());
            model.addRow(row);
            if (request.getReqStatus().equalsIgnoreCase("Accepted")) {
                totalFunds += ((GovFundReq) request).getAmountRequested();
            }
            }
        }

        txtTotFndRcvd.setText(String.valueOf(totalFunds));
        TableRowSorter<DefaultTableModel> sorter = new TableRowSorter<>(model);
        tblWrkReq.setRowSorter(sorter);
    }

