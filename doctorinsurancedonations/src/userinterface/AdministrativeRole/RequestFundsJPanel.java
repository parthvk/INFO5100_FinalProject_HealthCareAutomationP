    private void btnSendFundReqActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSendFundReqActionPerformed

        String location = txtLoc.getText().trim();
        String population = txtpop.getText();
        String amount = txtAmt.getText();
        if (location.equals("") || population.equals("") || amount.equals("")) {
            JOptionPane.showMessageDialog(null, "All fields are mandatory");
        } else {
            try {
                Integer.parseInt(population);
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(null, "Please type an Integer for population");
                return;
            }
            try {
                Double.parseDouble(amount);
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(null, "Please type an Integer value for amount");
                return;
            }
            GovFundReq governmentFundRequest = new GovFundReq(location, Integer.parseInt(population), Double.parseDouble(amount));
            governmentFundRequest.setSndr(usrAc);
            governmentFundRequest.setReqStatus("Sent");

            Organization org = null;

            List<Networks> networks = ecoSystem.getNetworks();

            for (Networks network : networks) {

                List<Enterprise> enterprises = network.getEntDir().getEnterpriseList();
                for (Enterprise enterprise : enterprises) {
                    List<Organization> organizations = enterprise.getOrgDir().getOrganizations();
                    for (Organization organization : organizations) {
                        if (organization instanceof OrgHealthCareOfficial) {
                            org = organization;
                            break;
                        }
                    }
                }
            }

            if (org != null) {

                org.getWrkQ().getWrkReqs().add(governmentFundRequest);
                usrAc.getWorkQ().getWrkReqs().add(governmentFundRequest);

            }
            pplTbl();
        }
    }//GEN-LAST:event_btnSendFundReqActionPerformed

